@dataclass
class AnalysisResults:
    """Results from structural analysis"""

    member_id: str
    load_case: str

    # Forces (design values)
    moment_y_max: float = 0.0  # [kNm]
    moment_y_min: float = 0.0  # [kNm]
    moment_z_max: float = 0.0  # [kNm]
    moment_z_min: float = 0.0  # [kNm]
    shear_y_max: float = 0.0  # [kN]
    shear_z_max: float = 0.0  # [kN]
    axial_max: float = 0.0  # [kN] (tension positive)
    axial_min: float = 0.0  # [kN] (compression negative)
    torsion_max: float = 0.0  # [kNm]

    # Deflections
    deflection_max: float = 0.0  # [m]
    deflection_y_max: float = 0.0
    deflection_z_max: float = 0.0

    # Reactions (for supports)
    reaction_fx: float = 0.0  # [kN]
    reaction_fy: float = 0.0
    reaction_fz: float = 0.0
    reaction_mx: float = 0.0  # [kNm]
    reaction_my: float = 0.0
    reaction_mz: float = 0.0


@dataclass
class ModelExport:
    """Export package for structural software"""

    software: StructuralSoftware
    model_name: str
    units: ModelUnits

    nodes: List[Dict[str, Any]]
    members: List[Dict[str, Any]]
    materials: List[Dict[str, Any]]
    sections: List[Dict[str, Any]]
    loads: List[Dict[str, Any]]
    load_combinations: List[Dict[str, Any]]

    export_format: str  # ".e2k", ".sdb", ".std"
    export_path: str
    exported_at: str = field(default_factory=lambda: datetime.now().isoformat())


