def export_from_ifc_to_analysis_software(
    ifc_file: str, software: StructuralSoftware, output_file: str
) -> ModelExport:
    """
    Complete workflow: IFC → ORION → Structural Software

    Args:
        ifc_file: Path to IFC file
        software: Target software
        output_file: Output file path

    Returns:
        ModelExport object
    """
    print("\n" + "=" * 80)
    print("IFC → ORION → STRUCTURAL SOFTWARE WORKFLOW")
    print("=" * 80)

    # Step 1: Extract from IFC
    print(f"\n1. Extracting structural model from IFC...")
    from structural_engineering_integration import extract_structural_model_from_ifc

    structural_model = extract_structural_model_from_ifc(ifc_file)
    nodes = structural_model["nodes"]
    members = structural_model["members"]

    # Step 2: Export to selected software
    print(f"\n2. Exporting to {software.value}...")

    connectors = {
        StructuralSoftware.ETABS: ETABSConnector(),
        StructuralSoftware.SAP2000: SAP2000Connector(),
        StructuralSoftware.STAAD_PRO: STAADProConnector(),
    }

    connector = connectors.get(software)
    if not connector:
        raise ValueError(f"Unsupported software: {software}")

    load_cases = [LoadCase("DEAD", "Eigenlast", "Eigenlast")]

    if software == StructuralSoftware.ETABS:
        export = connector.export_model(nodes, members, load_cases, output_file)
    else:
        export = connector.export_model(nodes, members, output_file)

    print("\n" + "=" * 80)
    print("✓ Workflow complete!")
    print("=" * 80)

    return export


