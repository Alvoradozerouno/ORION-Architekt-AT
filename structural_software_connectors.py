# ============================================================================


class ETABSConnector:
    """
    ETABS API Connector

    ETABS uses .e2k (text) or .edb (database) formats
    We'll use .e2k for easier generation
    """

    def __init__(self, model_name: str = "ORION_Model"):
        self.model_name = model_name
        self.units = ModelUnits.KN_M

    def export_model(
        self,
        nodes: List[StructuralNode],
        members: List[StructuralMember],
        load_cases: List[LoadCase],
        output_file: str,
    ) -> ModelExport:
        """
        Export structural model to ETABS .e2k format

        Args:
            nodes: List of structural nodes
            members: List of structural members
            load_cases: List of load cases
            output_file: Path to .e2k file

        Returns:
            ModelExport object
        """
        print(f"\n🔗 Exporting to ETABS: {output_file}")

        e2k_content = []

        # Header
        e2k_content.append("$ ETABS TEXT FILE")
        e2k_content.append(f"$ Model: {self.model_name}")
        e2k_content.append(f"$ Generated by ORION Architekt AT")
        e2k_content.append(f"$ Date: {datetime.now().strftime('%Y-%m-%d %H:%M:%S')}")
        e2k_content.append("")

        # Program Control
        e2k_content.append("PROGRAM CONTROL")
        e2k_content.append(f'   ModelName="{self.model_name}"')
        e2k_content.append(f'   Units="{self.units.value}"')
        e2k_content.append("")

        # Materials
        e2k_content.append("MATERIAL")
        unique_materials = self._extract_unique_materials(members)
        for mat in unique_materials:
            e2k_content.extend(self._material_to_e2k(mat))
        e2k_content.append("")

        # Frame Sections
        e2k_content.append("FRAME SECTION")
        unique_sections = self._extract_unique_sections(members)
        for section in unique_sections:
            e2k_content.extend(self._section_to_e2k(section))
        e2k_content.append("")

        # Joints (Nodes)
        e2k_content.append("JOINT COORDINATES")
        for node in nodes:
            e2k_content.append(self._node_to_e2k(node))
        e2k_content.append("")

        # Joint Restraints
        e2k_content.append("JOINT RESTRAINTS")
        for node in nodes:
            if node.restraints:
                e2k_content.append(self._restraint_to_e2k(node))
        e2k_content.append("")

        # Frame Elements
        e2k_content.append("FRAME CONNECTIVITY")
        for member in members:
            e2k_content.append(self._member_to_e2k(member))
        e2k_content.append("")

        # Load Patterns
        e2k_content.append("LOAD PATTERN DEFINITIONS")
        for lc in load_cases:
            e2k_content.append(self._loadcase_to_e2k(lc))
        e2k_content.append("")

        # Frame Loads
        e2k_content.append("FRAME LOADS - DISTRIBUTED")
        for member in members:
            if member.loads:
                for load in member.loads:
                    e2k_content.append(self._member_load_to_e2k(member, load))
        e2k_content.append("")

        # Write to file
        with open(output_file, "w") as f:
            f.write("\n".join(e2k_content))

        print(f"✓ Exported {len(nodes)} nodes, {len(members)} members to {output_file}")

        return ModelExport(
            software=StructuralSoftware.ETABS,
            model_name=self.model_name,
            units=self.units,
            nodes=[{"id": n.node_id, "x": n.x, "y": n.y, "z": n.z} for n in nodes],
            members=[{"id": m.member_id, "type": m.element_type.value} for m in members],
            materials=[],
            sections=[],
            loads=[],
            load_combinations=[],
            export_format=".e2k",
            export_path=output_file,
        )

    def _extract_unique_materials(self, members: List[StructuralMember]) -> List[Material]:
        """Extract unique materials from members"""
        materials_dict = {}
        for member in members:
            mat = member.cross_section.concrete
            if mat and mat.material_id not in materials_dict:
                materials_dict[mat.material_id] = mat
        return list(materials_dict.values())

    def _extract_unique_sections(self, members: List[StructuralMember]) -> List[CrossSection]:
        """Extract unique sections from members"""
        sections_dict = {}
        for member in members:
            sec = member.cross_section
            if sec.section_id not in sections_dict:
                sections_dict[sec.section_id] = sec
        return list(sections_dict.values())

    def _material_to_e2k(self, material: Material) -> List[str]:
        """Convert material to ETABS format"""
        lines = []
        lines.append(f'   Name="{material.material_id}"')
        lines.append(f'   Type="Concrete"')
        if material.fck:
            lines.append(f"   Fc={material.fck}")  # MPa
        if material.e_modulus:
            lines.append(f"   E={material.e_modulus}")  # MPa
        lines.append(f"   Density={material.density}")  # kN/m³
        return lines

    def _section_to_e2k(self, section: CrossSection) -> List[str]:
        """Convert cross-section to ETABS format"""
        lines = []
        lines.append(f'   Name="{section.section_id}"')
        lines.append(f'   Material="{section.concrete.material_id}"')
        lines.append(f'   Shape="Rectangular"')
        lines.append(f"   Depth={section.height}")  # m
        lines.append(f"   Width={section.width}")  # m
        return lines

    def _node_to_e2k(self, node: StructuralNode) -> str:
        """Convert node to ETABS format"""
        return f'   Joint="{node.node_id}"  X={node.x:.3f}  Y={node.y:.3f}  Z={node.z:.3f}'

    def _restraint_to_e2k(self, node: StructuralNode) -> str:
        """Convert restraints to ETABS format"""
        restraints = node.restraints
        ux = "Yes" if restraints.get("ux", False) else "No"
        uy = "Yes" if restraints.get("uy", False) else "No"
        uz = "Yes" if restraints.get("uz", False) else "No"
        rx = "Yes" if restraints.get("rx", False) else "No"
        ry = "Yes" if restraints.get("ry", False) else "No"
        rz = "Yes" if restraints.get("rz", False) else "No"

        return f'   Joint="{node.node_id}"  ' f"UX={ux} UY={uy} UZ={uz} RX={rx} RY={ry} RZ={rz}"

    def _member_to_e2k(self, member: StructuralMember) -> str:
        """Convert member to ETABS format"""
        return (
            f'   Frame="{member.member_id}"  '
            f'JointI="{member.start_node}"  '
            f'JointJ="{member.end_node}"  '
            f'Section="{member.cross_section.section_id}"'
        )

    def _loadcase_to_e2k(self, load_case: LoadCase) -> str:
        """Convert load case to ETABS format"""
        return f'   LoadPattern="{load_case.load_case_id}"  Type="{load_case.load_type}"'

    def _member_load_to_e2k(self, member: StructuralMember, load: Dict[str, Any]) -> str:
        """Convert member load to ETABS format"""
        load_type = load.get("type", "uniform")
        value = load.get("value", 0.0)
        direction = load.get("direction", "Gravity")

        return (
            f'   Frame="{member.member_id}"  '
            f'LoadPat="{load.get("load_case", "DEAD")}"  '
            f'Dir="{direction}"  '
            f'DistType="RelDist"  '
            f"RelDistA=0.0  "
            f"RelDistB=1.0  "
            f"FOverLA={value}  "
            f"FOverLB={value}"
        )

    def import_results(self, results_file: str) -> List[AnalysisResults]:
        """
        Import analysis results from ETABS

        In production: would use ETABS API or parse .e2k results
        Here: simulation
        """
        print(f"\n📊 Importing results from ETABS: {results_file}")

        # Simulated results
        results = [
            AnalysisResults(
                member_id="BEAM-1",
                load_case="COMB1",
                moment_y_max=45.2,
                moment_y_min=-38.5,
                shear_z_max=25.3,
                axial_max=0.0,
                axial_min=0.0,
                deflection_max=0.008,
            ),
            AnalysisResults(
                member_id="COL-1",
                load_case="COMB1",
                moment_y_max=12.5,
                moment_y_min=-10.2,
                shear_z_max=15.8,
                axial_max=0.0,
                axial_min=-185.5,  # compression
                deflection_max=0.002,
            ),
        ]

        print(f"✓ Imported {len(results)} result sets")

        return results


