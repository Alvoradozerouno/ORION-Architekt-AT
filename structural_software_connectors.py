class STAADProConnector:
    """
    STAAD.Pro API Connector

    STAAD.Pro uses .std (text-based command file)
    """

    def __init__(self, model_name: str = "ORION_Model"):
        self.model_name = model_name

    def export_model(
        self, nodes: List[StructuralNode], members: List[StructuralMember], output_file: str
    ) -> ModelExport:
        """
        Export structural model to STAAD.Pro .std format

        Args:
            nodes: List of structural nodes
            members: List of structural members
            output_file: Path to .std file

        Returns:
            ModelExport object
        """
        print(f"\n🔗 Exporting to STAAD.Pro: {output_file}")

        std_lines = []

        # Header
        std_lines.append("STAAD SPACE")
        std_lines.append(f"START JOB INFORMATION")
        std_lines.append(f"PROJECT {self.model_name}")
        std_lines.append(f"ENGINEER ORION Architekt AT")
        std_lines.append(f"END JOB INFORMATION")
        std_lines.append("")

        # Units
        std_lines.append("UNIT METER KN")
        std_lines.append("")

        # Joint Coordinates
        std_lines.append("JOINT COORDINATES")
        for node in nodes:
            std_lines.append(f"{node.node_id} {node.x:.3f} {node.y:.3f} {node.z:.3f}")
        std_lines.append("")

        # Member Incidences
        std_lines.append("MEMBER INCIDENCES")
        for i, member in enumerate(members, 1):
            start_id = member.start_node.replace("N", "")
            end_id = member.end_node.replace("N", "")
            std_lines.append(f"{i} {start_id} {end_id}")
        std_lines.append("")

        # Supports
        std_lines.append("SUPPORTS")
        for node in nodes:
            if node.restraints and any(node.restraints.values()):
                node_num = node.node_id.replace("N", "")
                std_lines.append(f"{node_num} FIXED")
        std_lines.append("")

        # Material
        std_lines.append("DEFINE MATERIAL START")
        std_lines.append("ISOTROPIC CONCRETE")
        std_lines.append("E 3.0e7")  # kN/m²
        std_lines.append("POISSON 0.2")
        std_lines.append("DENSITY 25")  # kN/m³
        std_lines.append("END DEFINE MATERIAL")
        std_lines.append("")

        # Member Properties
        std_lines.append("MEMBER PROPERTY")
        for i, member in enumerate(members, 1):
            w = member.cross_section.width * 1000  # to mm
            h = member.cross_section.height * 1000
            std_lines.append(f"{i} PRIS YD {h} ZD {w}")
        std_lines.append("")

        # Finish
        std_lines.append("FINISH")

        with open(output_file, "w") as f:
            f.write("\n".join(std_lines))

        print(f"✓ Exported {len(nodes)} nodes, {len(members)} members to {output_file}")

        return ModelExport(
            software=StructuralSoftware.STAAD_PRO,
            model_name=self.model_name,
            units=ModelUnits.KN_M,
            nodes=[{"id": n.node_id, "x": n.x, "y": n.y, "z": n.z} for n in nodes],
            members=[{"id": m.member_id, "type": m.element_type.value} for m in members],
            materials=[],
            sections=[],
            loads=[],
            load_combinations=[],
            export_format=".std",
            export_path=output_file,
        )


