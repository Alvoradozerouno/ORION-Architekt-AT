# Universal Connector (Auto-detect)
# ============================================================================


class UniversalConnector:
    """
    Universal connector that auto-selects the appropriate software connector
    """

    def __init__(self, model_name: str = "ORION_Model"):
        self.model_name = model_name
        self.etabs = ETABSConnector(model_name)
        self.sap2000 = SAP2000Connector(model_name)
        self.staad = STAADProConnector(model_name)

    def export_all(
        self,
        nodes: List[StructuralNode],
        members: List[StructuralMember],
        load_cases: List[LoadCase],
        output_dir: str = ".",
    ) -> Dict[str, ModelExport]:
        """
        Export to all supported formats

        Args:
            nodes: List of nodes
            members: List of members
            load_cases: List of load cases
            output_dir: Output directory

        Returns:
            Dict of exports by software
        """
        print("\n" + "=" * 80)
        print("UNIVERSAL EXPORT - All Structural Software Formats")
        print("=" * 80)

        exports = {}

        # ETABS
        etabs_file = f"{output_dir}/{self.model_name}_ETABS.e2k"
        exports["ETABS"] = self.etabs.export_model(nodes, members, load_cases, etabs_file)

        # SAP2000
        sap_file = f"{output_dir}/{self.model_name}_SAP2000.json"
        exports["SAP2000"] = self.sap2000.export_model(nodes, members, sap_file)

        # STAAD.Pro
        staad_file = f"{output_dir}/{self.model_name}_STAAD.std"
        exports["STAAD.Pro"] = self.staad.export_model(nodes, members, staad_file)

        print("\n" + "=" * 80)
        print(f"✓ Exported to {len(exports)} formats")
        print("=" * 80)

        return exports


